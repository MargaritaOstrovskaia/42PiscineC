int	ft_iterative_factorial(int nb)
{
	int	f;

	if (nb >= 0 && nb <= 12)
	{
		f = 1;
		while (nb != 0)
		{
			f *= nb;
			nb--;
		}
		return (f);
	}
	else
		return (0);
}
